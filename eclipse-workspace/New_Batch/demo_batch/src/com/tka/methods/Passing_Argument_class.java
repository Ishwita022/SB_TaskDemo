package com.tka.methods;

public class Passing_Argument_class {
public void add(int a, int b) {
	int c=a+b;
	System.out.println(c);
}

public void sub(int a1, int b1) {
	int c=a1-b1;
	System.out.println(c);
}

public void mul(int a2, int b2) {
	int c=a2*b2;
	System.out.println(c);
}

public void div(int a3, int b3) {
	int c=a3/b3;
	System.out.println(c);
}

public void mod(int a4, int b4) {
	int c=a4%b4;
	System.out.println(c);
}
}
